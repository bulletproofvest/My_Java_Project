package ch01_variable_operator;

public class Trapeziod {
    public static void main(String[] args) {
        double bot = 20;
        double top = 10;
        double hei = 15;
        double area = (bot+top)*hei/2;

        System.out.println("밑변 : " + bot);
        System.out.println("윗변 : " + top);
        System.out.println("높이 : " + hei);
        System.out.println("넓이 : " + area);


    }
}
