package ch01_variable_operator;

public class LogicalOperator {
    public static void main(String[] args) {
        int x = 3;
        int y = 2;

        System.out.println("x = "+x+", y = " + y);
        System.out.println("(x >= y) : " + (x >= y));
        System.out.println("!(x >= y) : " + !(x >= y));
        System.out.println("(x == y) : " + (x == y));
        System.out.println("(x != y) : " + (x != y));
        System.out.println("수식 : " + (x>y));
        System.out.println("수식 : " + (x>y));
    }
}
